package sec01_exam;

public class VarEx1 {

	public static void main(String[] args) {
		
		int year = 0;
		int age = 14;
		
		System.out.printf("%d %d %n", year, age);
		System.out.println(year);
		System.out.println(age+"yo");
		
		System.out.println();
		
		year = age + 2000;
		age = age + 1;
		
		System.out.println(year);
		System.out.print(age);
		
	}

}
